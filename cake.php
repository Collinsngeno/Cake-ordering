<img src="blackforest.jpg" />
<li>Black Forest cake consists of several layers of chocolate cake, with whipped cream and cherries between each layer. Then the cake is decorated with additional whipped cream, maraschino cherries, and chocolate shavings. In some European traditions sour cherries are used both between the layers and for decorating the top.Traditionally, kirsch (a clear liquor distilled from tart cherries) is added to the cake,although other liquors are also used (such as rum, which is common in Austrian recipes). In North America, Black Forest cake is generally prepared without alcohol. </li>
